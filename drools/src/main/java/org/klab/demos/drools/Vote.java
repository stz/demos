package org.klab.demos.drools;

public class Vote {

	private String vote;
	
	private float average;

	public String getVote() {
		return this.vote;
	}

	public void setVote(String vote) {
		this.vote = vote;
	}

	public float getAverage() {
		return this.average;
	}

	public void setAverage(float average) {
		this.average = average;
	}
}